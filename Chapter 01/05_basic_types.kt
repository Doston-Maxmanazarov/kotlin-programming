fun main() {
    val binary = 0b01

    val oneByt: Byte = 1        // Byte

    val oneShort: Short = 1     // Short

    val oneInt = 1              // Int
    val twoInt: Int = 2         // Int

    val oneLong = 1L            // Long
    val twoLong: Long = 2       // Long

    val oneDouble: Double = 1.0 // Double
    val pi = 3.14               // Double

    val oneFloat = 1f           // Float

    println(binary)     // 1
    println(oneByt)     // 1
    println(oneShort)   // 1
    println(oneInt)     // 1
    println(twoInt)     // 2
    println(oneLong)    // 1
    println(twoLong)    // 2
    println(oneDouble)  // 1.0
    println(pi)         // 3.14
    println(oneFloat)   // 1.0
}