fun main() {
    val a = 10
    val b = 2

    val sum = a + b // 10+2=12
    val difference = a - b // 10-2=8
    val division = a / b // 10/2=5
    val multiplication = a * b // 10*2=20
    val modulus = a % b // 10%2=0

    println("sum = $sum") // sum = 12
    println("difference = $difference") // difference = 8
    println("division = $division") // division = 5
    println("multiplication = $multiplication") // multiplication = 20
    println("modulus = $modulus") // modulus = 0
}